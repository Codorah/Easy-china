// ─── REACTIVE LANG STORE ──────────────────────────────────────────────────────
const _listeners = new Set();

let _lang = (() => {
  try {
    const stored = localStorage.getItem("ec_lang");
    if (stored && TRANSLATIONS[stored]) return stored;
    const browser = (navigator.language || "fr").slice(0, 2).toLowerCase();
    return TRANSLATIONS[browser] ? browser : "fr";
  } catch {
    return "fr";
  }
})();

export const getLang = () => _lang;

export const changeLang = (code) => {
  if (!TRANSLATIONS[code]) return;
  _lang = code;
  try { localStorage.setItem("ec_lang", code); } catch {}
  _listeners.forEach((fn) => fn(code));
};

export const t = (key) =>
  TRANSLATIONS[_lang]?.[key] ?? TRANSLATIONS.fr?.[key] ?? key;

export function useLang() {
  const [, setTick] = useState(0);
  useEffect(() => {
    const handler = () => setTick((n) => n + 1);
    _listeners.add(handler);
    return () => _listeners.delete(handler);
  }, []);
  return _lang;
}
